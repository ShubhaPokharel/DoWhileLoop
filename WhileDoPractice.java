class WhileDoPractice{
	public static void main(String[] args){
	do{
	System.out.println("Good Morning!");
	break;
	}
	while (5<2);

	System.out.println("***********************************");

	do{
	System.out.println("Good Evening!");
	}
	while (5<2);

    //infinite loop
	do{
	System.out.println("Good Night!");
	}
	while (5>2);
	}
}